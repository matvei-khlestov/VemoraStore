import Foundation

/// Протокол `PreferencesStore`
///
/// Определяет интерфейс для хранения и получения пользовательских настроек приложения
/// (например, последних выбранных фильтров, токенов сессии или параметров отображения UI).
///
/// Основные задачи:
/// - сохранение строковых значений (`set`);
/// - извлечение ранее сохранённых значений (`string`);
/// - удаление значений (`remove`).
///
/// Используется в:
/// - `CheckoutStorage` для реализации хранения через `UserDefaults`;
/// - настройках, состояниях фильтров и других вспомогательных сервисах приложения.

protocol PreferencesStore {
    
    /// Сохраняет значение по заданному ключу.
    /// - Parameters:
    ///   - value: Строка, которую нужно сохранить.
    ///   - key: Ключ, под которым значение будет храниться.
    func set(_ value: String, forKey key: String)
    
    /// Возвращает сохранённое значение по ключу.
    /// - Parameter key: Ключ, по которому требуется получить значение.
    /// - Returns: Строковое значение или `nil`, если оно не найдено.
    func string(forKey key: String) -> String?
    
    /// Удаляет значение из хранилища.
    /// - Parameter key: Ключ удаляемого значения.
    func remove(_ key: String)
}
