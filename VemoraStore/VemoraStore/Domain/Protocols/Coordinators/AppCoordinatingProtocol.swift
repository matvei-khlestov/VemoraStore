import Foundation

/// Протокол верхнеуровневого координатора приложения.
///
/// `AppCoordinatingProtocol` наследуется от базового `Coordinator`
/// и определяет точку входа в приложение. Этот координатор отвечает
/// за выбор начального флоу — авторизацию, онбординг или главный экран.
///
/// ## Основные задачи:
/// - Управление стартовой логикой приложения.
/// - Инициализация корневого координатора (`AuthCoordinator`, `MainCoordinator` и т.д.).
/// - Переключение между основными потоками (например, logout / login).
///
/// Таким образом, `AppCoordinatingProtocol` задаёт единый контракт
/// для управления жизненным циклом приложения и навигацией между его основными флоу.
protocol AppCoordinatingProtocol: Coordinator {
    /// Запускает основной флоу приложения (например, авторизацию или главный экран).
    func start()
}
