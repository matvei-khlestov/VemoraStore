import Foundation

/// Протокол координатора экрана профиля пользователя.
///
/// Определяет контракт для навигации внутри раздела профиля.
/// Используется для управления переходами между экранами редактирования данных,
/// заказов, информации о приложении и политике конфиденциальности.
///
/// Также содержит колбэки для обработки выхода из аккаунта (`onLogout`)
/// и удаления учётной записи (`onDeleteAccount`).

protocol ProfileUserCoordinatingProtocol: Coordinator {
    
    /// Запускает основной экран профиля пользователя.
    func start()
    
    /// Замыкание, вызываемое при выходе из аккаунта.
    var onLogout: (() -> Void)? { get set }
    
    /// Замыкание, вызываемое при удалении учётной записи.
    var onDeleteAccount: (() -> Void)? { get set }
}
